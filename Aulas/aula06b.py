algo = input('Digite algo: ')

print('O tipo é: ', type(algo))

print('É a alfabético: ', algo.isalpha())
print('É numérico: ', algo.isnumeric())
print('É alfanumérico: ', algo.isalnum())
print('Está escrito todo em maiúsculo', algo.isupper())
print('Está escrito todo em minúculo: ', algo.islower())


